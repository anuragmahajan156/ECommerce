package com.ecommerce;

public class Staff {
	private String staffName;
	private String staffPhoneNo;
	private String staffEmailId;
	private String staffUserName;
	private String staffLoginPassword;
	
	public String getStaffName() {
		return staffName;
	}
	public void setStaffName(String staffName) {
		this.staffName = staffName;
	}
	public String getStaffPhoneNo() {
		return staffPhoneNo;
	}
	public void setStaffPhoneNo(String staffPhoneNo) {
		this.staffPhoneNo = staffPhoneNo;
	}
	public String getStaffEmailId() {
		return staffEmailId;
	}
	public void setStaffEmailId(String staffEmailId) {
		this.staffEmailId = staffEmailId;
	}
	public String getStaffUserName() {
		return staffUserName;
	}
	public void setStaffUserName(String staffUserName) {
		this.staffUserName = staffUserName;
	}
	public String getStaffLoginPassword() {
		return staffLoginPassword;
	}
	public void setStaffLoginPassword(String staffLoginPassword) {
		this.staffLoginPassword = staffLoginPassword;
	}
	
}
