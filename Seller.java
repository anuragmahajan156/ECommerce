package com.ecommerce;

public class Seller {
	private String sellerName;
	private String sellerAddress;
	private String sellerPhoneNo;
	private String sellerEmailId;
	private int productCat;
	
	private String sellerUserName;
	private String sellerLoginPassword;
	
	public String getSellerUserName() {
		return sellerUserName;
	}
	public void setSellerUserName(String sellerUserName) {
		this.sellerUserName = sellerUserName;
	}
	public String getSellerLoginPassword() {
		return sellerLoginPassword;
	}
	public void setSellerLoginPassword(String sellerLoginPassword) {
		this.sellerLoginPassword = sellerLoginPassword;
	}
	public String getSellerName() {
		return sellerName;
	}
	public void setSellerName(String sellerName) {
		this.sellerName = sellerName;
	}
	public String getSellerAddress() {
		return sellerAddress;
	}
	public void setSellerAddress(String sellerAddress) {
		this.sellerAddress = sellerAddress;
	}
	public String getSellerPhoneNo() {
		return sellerPhoneNo;
	}
	public void setSellerPhoneNo(String sellerPhoneNo) {
		this.sellerPhoneNo = sellerPhoneNo;
	}
	public String getSellerEmailId() {
		return sellerEmailId;
	}
	public void setSellerEmailId(String sellerEmailId) {
		this.sellerEmailId = sellerEmailId;
	}
	public int getProductCat() {
		return productCat;
	}
	public void setProductCat(int productCat) {
		this.productCat = productCat;
	}
	
	
}
